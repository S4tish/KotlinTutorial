// Kotlin While Loop

// Loops
// Loops can execute a block of code as lonf as a specified condition is reached

// Loops are handy because they save time reduce erors and they make code more readavle

fun main(){
    var i=0;
    while(i<5){
        println(i)
        i++
    }
}

// The Do while Loop
// The do while loop is a variant of the while loop.This loop will execute the code block once.
// before checking if the condition is true then it will repeat the loop as long as the condition
// is true

fun main(){
    var i=0;
    do{
        println(i)
        i++
    }
    while(i<5)
}